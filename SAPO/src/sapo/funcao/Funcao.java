package sapo.funcao;

/**
 * Interface de funções. Interface a ser implementada pelas diferentes funções
 * que uma pessoa pode assumir no sistema, como Aluno e Professor, por exemplo.
 * 
 * @author Jônatas Tavares dos Santos - 121110769
 */
public interface Funcao {

	/**
	 * Representação textual da função. Método que retorna uma string com a
	 * representação textual a ser implementado por uma função.
	 * 
	 * @return String com a representação textual.
	 */
	public String toString();
}
